package collectionFramework;



	import java.util.HashSet;

	public class HashSetClass {
		public static void main(String[] args) {
			System.out.println("HashSet program running");
			HashSet hs = new HashSet();
			hs.add("Pooja"); //String
			hs.add("Hule"); //String
			hs.add('A'); //Char
			hs.add(9860421557L);
			hs.add(80.20); //float
			hs.add("Pune"); //String
			hs.add(411028); //int
			hs.add(null); //String
			hs.add(null); //String
			hs.add(null); //String
			hs.add(true); //boolean
			hs.add("Pooja"); //String
			hs.add("Hule"); //String
			System.out.println(hs);
			
		}
	}

	

	
		
	


